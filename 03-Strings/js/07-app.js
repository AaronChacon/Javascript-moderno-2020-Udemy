const producto = 'Monitor 20 pulgadas';

//toUpperCase
/* El toUpperCase() método devuelve el valor convertido en 
mayúsculas de la cadena que realiza la llamada. */

console.log(producto.toUpperCase());

//toLowerCase
/* El método toLowerCase() devuelve el valor en minúsculas 
de la cadena que realiza la llamada. Sintaxis */

console.log(producto.toLowerCase());

